    private void mniSaveMatrixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniSaveMatrixActionPerformed
        File saveFile = getSaveFile(new FileNameExtensionFilter("Graph data (*.mtx)", "mtx"), "graph_" + sdfDate.format(Calendar.getInstance().getTime()) + ".mtx");
        //1 Cần lưu số lượng đỉnh của đồ thì (NumberofVertices)
        //2 Lưu tọa độ x,y của các đỉnh
        //3 ma trận trọng số
        if (saveFile != null) {
            if (saveFile.getName().endsWith(".mtx")) {
                ArrayList<GVertex> verties = p.getVertices();
                int[][] graph = p.getGraph();
                int numberOfVertex = p.getNumberOfVertices();
                FileWriter fw = null;
                String strData = "";
                String strXY = "";

                for (int i = 0; i < verties.size(); i++) {
                    strXY += "\n" + verties.get(i).getX() + " " + verties.get(i).getY();
                }

                try {
                    strData += numberOfVertex;
                    strData += strXY;
                    for (int i = 0; i < numberOfVertex; i++) {
                        strData += "\n" + graph[i][0];
                        for (int j = 1; j < numberOfVertex; j++) {
                            strData += " " + graph[i][j];
                        }
                    }
                    fw = new FileWriter(saveFile);
                    fw.write(strData);
                    fw.flush();
                    fw.close();
                } catch (IOException e) {
                    System.err.println(e);
                } finally {
                    try {
                        fw.close();
                    } catch (Exception e) {
                        System.err.println(e);
                    }
                }
            }
        }
    }//GEN-LAST:event_mniSaveMatrixActionPerformed

    private void mniListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniListActionPerformed
        File saveFile = getSaveFile(new FileNameExtensionFilter("Graph data (*.lst)", "lst"), "graph_" + sdfDate.format(Calendar.getInstance().getTime()) + ".lst");

        if (saveFile != null) {
            if (saveFile.getName().endsWith(".lst")) {
                ArrayList<GVertex> verties = p.getVertices();
                int[][] graph = p.getGraph();
                int numberOfVertex = p.getNumberOfVertices();
                FileWriter fw = null;
                String strData = "";
                String strXY = "";

                for (int i = 0; i < verties.size(); i++) {
                    strXY += "\n" + verties.get(i).getX() + " " + verties.get(i).getY();
                }

                try {
                    int countEdge = 0;
                    for (int i = 0; i < numberOfVertex - 1; i++) {
                        for (int j = i + 1; j < numberOfVertex; j++) {
                            if (graph[i][j] > 0) {
                                strData += "\n" + i + " " + j + " " + graph[i][j];
                                ++countEdge;
                            }
                        }
                    }
                    strData = numberOfVertex + " " + countEdge + strXY + strData;
                    fw = new FileWriter(saveFile);
                    fw.write(strData);
                    fw.flush();
                    fw.close();
                } catch (IOException e) {
                    System.err.println(e);
                } finally {
                    try {
                        fw.close();
                    } catch (Exception e) {
                        System.err.println(e);
                    }
                }
            }
        }
    }//GEN-LAST:event_mniListActionPerformed

    private void mniOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniOpenActionPerformed
        fileChooser.setFileFilter((new FileNameExtensionFilter("Graph data (*.lst,*.mtx)", "lst", "mtx")));
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            if (!fileChooser.getSelectedFile().exists()) {
                return;
            }
            File openFile = fileChooser.getSelectedFile();
            if (openFile.getName().endsWith(".mtx")) {
                p.readMatrixDataFile(openFile);
            } else if (openFile.getName().endsWith(".lst")) {
                p.readListDataFile(openFile);
            }
        }
    }//GEN-LAST:event_mniOpenActionPerformed

